				<p>Depois de quase 25 anos de uma experiência extraordinária na área de comunicação de marketing, durante a qual teve a oportunidade de estar “nos dois lados da mesa”, de atuar em todas etapas do processo de comunicação, desde a definição da estratégia, como executivo de marketing, até a criação e execução da ação ou campanha,como diretor de criação, Marcos decidiu começar uma nova carreira, na qual poderia exercer o que considera sua verdadeira vocação: ajudar pessoas e organizações a terem condições de existir plenamente, com o máximo aproveitamento de seu potencial, dependendo apenas de sua própria decisão para terem sucesso.</p>
