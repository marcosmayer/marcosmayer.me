				<h2><span>Minha primeira vez</span></h2>
				<p>Dizem que a primeira vez a gente nunca esquece. Certamente, Marcos nunca esquecerá a sua.</p>
				<p>Sua primeira experiência como palestrante profissional foi marcante. No início de 2015, ele foi convidado a dar uma palestra em Franca, no interior de São Paulo, pelo maior jornal da região. Marcos se preparou. Sua expectativa era muito grande, mas ela foi ainda superada. E muito!</p>
				<p>Quando chegou o dia, tudo correu bem, conforme planejado. Mas depois da palestra, algumas coisas legais aconteceram.</p>
				<p>Primeiro, um importante político da região perguntou, em tom de brincadeira, se Marcos pretendia concorrer a algum cargo público na região, porque ele estava preocupado com a concorrência. Em seguida, o diretor institucional e de conteúdo do jornal o abordou e disse que seu currículo, que ele havia enviado para divulgação, não o representava adequadamente. Marcos ficou preocupado, imaginando que pudesse ter frustrado a expectativa gerada pelo material enviado, mas era bem o contrário. O diretor havia achado que ele era bem melhor do que o seu currículo indicava. Ali mesmo, Marcos foi convidado para ser colunista do jornal, o que, de fato, aconteceu por algum tempo. Seu terceito encontro depois da palestra foi com um casal que atuava, voluntariamente, nas escolas da região. Eles faziam parte da Ordem dos Advogados do Brasil (OAB) de Franca, que tinha um programa chamado OAB na Escola, e convidaram Marcos para dar uma palestra na cerimônia de premiação de um concurso de redação que promoviam nas escolas, o que ele prontamente aceitou, já estabelecendo que não cobraria por aquela participação.</p>
				<p>No instante em que Marcos chegou ao seu quarto no hotel e deitou em sua cama, física e emocionalmente cansado, mas muito realizado, ele soube que era isso que ele queria fazer como seu trabalho.</p>
