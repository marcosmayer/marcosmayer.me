			<div class="produto-texto-area">
				<p><span class="destaque">Design Thinking: a Inovação na Inovação</span> é mais do que um workshop sobre inovação. É um momento marcante na vida dos profissionais e da organização envolvida. É a mudança do jeito antigo para o novo jeito de fazer as coisas.</p>
				<p>Os participantes do workshop serão treinados na solução criativa de problemas, incluindo como reconhecer e definir o escopo correto desses problemas, quer sejam demandas do mercado, quer sejam descobertas nas quais ninguém pensou antes. Eles aprenderão a trabalhar de forma colaborativa para transformar esses problemas em oportunidades de crescimento para a organização.</p>
				<p>Por meio de um workshop que é, ao mesmo tempo, divertido, interativo e provocador, os membros da equipe aprenderão a fundo o conceito de design thinking e sua aplicação prática como uma metodologia para a geração, avaliação e implementação de ideias originais.</p>
				<p>Os participantes vão pensar, reagir, divertir-se, rir, compartilhar, aprender e mudar. Eles sairão do workshop com um novo conjunto de habilidades que aumentará em muito sua capacidade de inovar. Além disso, e mais importante, eles estarão prontos e com muita vontade de aplicar essas novas ideias no seu trabalho e na sua empresa, o mais cedo possível.</p>
