				<h2><span>Criatividade e Inovação</span></h2>
				<p>A criatividade e inovação são as duas capacidades que nos trouxeram até aqui. E foi uma longa jornada, desde os primeiros passos dados por humanos no nosso planeta. Desde então, muita coisa mudou e continua mudando, em um ritmo cada vez mais acelerado. Essas duas grandes amigas, que têm muitas histórias juntas, são inseparáveis e se completam perfeitamente.</p>
				<p>Primeiro, entra em cena a criatividade, com sua insaciável curiosidade e ilimitada imaginação. Suas ideias são incríveis, originais e capazes de resolver qualquer problema que a humanidade já enfrentou, está enfrentando ou enfrentará no futuro. Sempre existe um jeito de fazer algo melhor ou de um jeito que nunca foi feito antes. São ideias valiosas, que mudam a vida das pessoas. É nisso que a criatividade brilha.</p>
				<p>Logo em seguida, a inovação chega pra valer. Recebe a ideia da amiga criatividade e encontra um jeito de aplicá-la e de colocá-la em prática. Nada pode detê-la, porque ela sabe que se a ideia veio da criatividade, ela pode tornar-se uma realidade. Claro que não é fácil, mas a criatividade encontrou um meio, e a inovação vai fazer acontecer.</p>
			</div>
		</div>
