				<h2><span>Muito além dos negócios</span></h2>
				<p>No dia 21 de abril de 2018, foi celebrado o primeiro <span class="destaque">Dia Mundial da Criatividade e Inovação</span>. Ele foi criado pela <span class="bold">Organização das Nações Unidas</span> (ONU), para encorajar o pensamento multidisciplinar, para nos ajudar a conquistar o futuro sustentável que nós queremos.</p>
				<p>De acordo com as descobertas da edição especial do <span class="bold">Relatório da Economia Criativa</span> intitulado <span class="destaque">“Ampliando Caminhos para o Desenvolvimento Local”</span>, co-publicado pela <span class="bold">UNESCO</span>, a <span class="bold">Organização para a Educação, a Ciência e a Cultura das Nações Unidas</span> e pelo <span class="bold">UNDP</span>, o <span class="bold">Programa de Desenvolvimento das Nações Unidas</span>, <span class="destaque">a criatividade e a inovação, tanto no nível individual, quanto no coletivo, tornaram-se a verdadeira riqueza das nações no século XXI</span>.</p>
				<p>No <span class="bold">Dia Mundial da Criatividade e Inovação</span>, o mundo foi convidado a abraçar a ideia de que a inovação é essencial para aproveitar o potencial econômico das nações. Inovação, criatividade e empreendedorismo em massa podem prover um novo tempo de crescimento econômico e criação de oportunidades de trabalho. Pode proporcionar soluções para alguns dos nossos problemas mais urgentes, como a erradicação da pobreza e a eliminação da fome.</p>
