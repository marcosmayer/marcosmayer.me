			<div class="text-block">
				<h2><span>Criatividade e inovação no trabalho</span></h2>
				<p>A minha definição de criatividade preferida é a do <span class="bold">Sir Ken Robinson</span>. Ela afirma que <span class="destaque">“criatividade é o processo de gerar ideias originais que tenham valor”</span>. A criatividade trata de acessar o potencial da mente para conceber ideias novas. A criatividade depende da imaginação, da curiosidade, do conhecimento e da avaliação.</p>
				<p>Inovação é a implementação de algo novo. Inovação é sobre implementar mudanças em sistemas relativamente estáveis. É o esforço necessário para tornar uma ideia viável.</p>
				<p>A criatividade trata das ideias, enquanto a inovação trata da aplicação das ideias. Assim, podemos concluir que a inovação é a criatividade aplicada. Criatividade é imaginar. Inovação é fazer.</p>
				<p>Se trouxermos a imaginação para dentro desse cenário, podemos dizer que a criatividade é colocar a imaginação para trabalhar. É a imaginação aplicada. E inovação é a criatividade aplicada.</p>
				<p>Mas por que a inovação é tão importante para as empresas, e cada vez mais urgente?</p>
			</div>
		</div>
		<div class="text-block-dark">
			<div class="text-block">
				<h2><span>A urgência da inovação</span></h2>
				<p><span class="bold">Peter Drucker</span> disse o seguinte sobre a inovação nos negócios: <span class="destaque">“Toda organização precisa preparar-se para abandonar tudo o que ela faz”</span>. É uma afirmação pesada, radical, mas como ela veio de quem veio, não podemos ignorá-la.</p>
				<p>É disso que falamos quando afirmamos que o nosso mundo está em constante mudança. É isso que <span class="bold">Peter Drucker</span> nos avisa. Não adianta tentar resolver problemas novos de jeitos antigos. E, mais especificamente na frase dele, não adianta tentar atender a demandas novas com produtos antigos.</p>
				<p>A questão com as mudanças é que elas não acontecem de forma linear, mas de forma exponencial. Não se trata de um problema novo aparecendo amanhã, mais um depois de amanhã e outro no dia depois. A mudança é exponencial. Acelerada.</p>
				<p>Felizmente, da mesma forma que as mudanças chegam em ritmo exponencial, as informações e o conhecimento disponível também são gerados da mesma forma.</p>
				<p>É por isso que a inovação é tão importante e urgente. Nas palavras de <span class="bold">Peter Drucker</span>, novamente (é, o homem conhecia muito do assunto!), <span class="destaque">“em tempos de turbulência, o maior perigo não é a turbulência em si, mas agir com a lógica de antigamente”</span>.</p>
