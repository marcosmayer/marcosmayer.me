							<div class="blog-post-titulo-read"><h1>Não existe um padrão para o sucesso</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">4 de abril de 2016</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-nao-existe-um-padrao-para-o-sucesso.jpg"></div>
							<div class="blog-post-texto">
								<p>Eu servi ao Exército Brasileiro, em 1991. Eu era um soldado do 2º Batalhão de Polícia do Exército. Foi um ano interessante, em que fui obrigado a interromper os estudos — eu estava no 3º colegial (ensino médio) — para ser preparado para defender o País, caso necessário.</p>
								<p>O Exército funciona baseado em dois alicerces muito importantes: a hierarquia e a disciplina. E isso faz todo sentido para uma organização militar. É necessário que seja assim.</p>
								<p>Vou dar uma dica importante para quem vai servir ao Exército: seja um “moita”. Um moita, entre os militares, é aquele que não se destaca em nada. O moita não é notado por ninguém, e isso, no Exército, é uma coisa boa. Quando marchamos, dezenas e até mesmo centenas de soldados fazendo tremer o chão com a sola de seus coturnos, estamos fazendo “ordem unida”, que é o nome dado a esse tipo de exercício. Não se destacar, na ordem unida, é essencial.</p>
								<p>Quando um militar faz algo digno de elogio, seja exibir um uniforme perfeitamente engomado e passado ou acertar o alvo com perfeição no exercício de tiro, a expressão que se usa para elogiá-lo é: “Padrão, soldado. Parabéns”. Estar o mais próximo possível do padrão é o melhor que se pode fazer, dentro do Exército.</p>
								<p>Só que a escola não é o Exército. E não deveria haver um padrão de performance e comportamento que seja a meta a ser atingida por todos.</p>
								<p>Se ensinamos aos alunos que o que eles precisam fazer é atingir a média em um teste padronizado, como podemos esperar que eles se sintam motivados?</p>
								<p>A motivação depende de fatores como autonomia, controle e propósito. São estas características que fazem com que um ambiente seja favorável à expressão da criatividade, no qual alunos encontram relevância no aprendizado e professores recebem o suporte para serem inovadores.</p>
								<p>Infelizmente, ainda somos forçados a — desculpem o trocadilho — marchar na direção oposta.</p>
