							<div class="blog-post-titulo-read"><h1>Alimentando Mentes Incríveis</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">12 de fevereiro de 2015</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-alimentando-mentes-incriveis.jpg"></div>
							<div class="blog-post-texto">
								<p>Crianças são seres incríveis. Sua capacidade de imaginar e expressar sua criatividade não tem limites. Sua curiosidade e a vontade de querer saber tudo alimentam suas mentes, verdadeiras máquinas de produzir ideias brilhantes.</p>
								<p>E elas continuam assim, até que nós – adultos, mais sabidos – acabamos com essa história de curiosidade aguçada e de ideias que “não levam a lugar algum”. E assim transformamos mais uma criança em um adulto como nós. Como se isso fosse uma coisa boa.</p>
								<p>Acontece que a criatividade, que é uma capacidade com a qual todos os seres humanos nascem, é uma habilidade essencial para se resolver problemas. E nunca estivemos tão carentes de pessoas capazes de resolver problemas quanto hoje.</p>
								<p>Sir Ken Robinson afirma que nós deseducamos as crianças de sua criatividade na escola. Em vez de desenvolvê-la, seguimos um currículo, que nos é imposto e que é incapaz de conectar estudantes ao mundo em que vivemos. Sem que nossas crianças conheçam o contexto e saibam quais problemas enfrentamos, elas nunca se sentirão incomodadas e desafiadas a efetuar mudanças.</p>
								<p>Uma pesquisa realizada pela consultoria John Snow Brasil, em 2008, revela que o uso do jornal na sala de aula melhora o desempenho dos estudantes, não só na escola, mas também em seu desenvolvimento social e pessoal. Segundo Miguel Fontes, diretor da John Snow Brasil, “quando você desmitifica o jornal para o jovem, ele gosta, percebe o mundo. Sente-se mais atuante e mais crítico”.</p>
								<p>Esta é uma questão muito séria em um país em que apenas uma em cada quatro pessoas é plenamente alfabetizada, capaz de acessar, analisar, avaliar e reagir às informações na mídia. Até entre estudantes de nível superior, 38% não têm essa capacidade.</p>
								<p>O caminho para um país melhor passa pela educação. Uma educação criativa, que prepara as crianças para a vida, não apenas para passar na prova.</p>
