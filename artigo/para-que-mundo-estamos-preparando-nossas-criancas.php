							<div class="blog-post-titulo-read"><h1>Para que mundo estamos preparando nossas crianças?</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">17 de agosto de 2016</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-para-que-mundo-estamos-preparando-nossas-criancas.jpg"></div>
							<div class="blog-post-texto">
								<p>Antes de tentarmos responder a questão de se estamos, como sociedade, preparando adequadamente nossas crianças no sistema educacional, precisamos refletir sobre outra questão, cuja resposta impacta diretamente o primeiro debate e que, portanto, precisa ser respondida antes.</p>
								<p>Para podermos fazer a pergunta certa, vamos considerar o seguinte. Segundo Richard Buckminster Fuller (1895 -1983), até 1900 o conhecimento humano dobrou aproximadamente a cada século. De 1900 até o final da Segunda Guerra Mundial, o mesmo aconteceu a cada 25 anos. Hoje, o conhecimento humano dobra a cada 13 meses. E, de acordo com a IBM, a chegada da “internet das coisas” vai levar à duplicação do conhecimento a cada 12 horas.</p>
								<p>Com base nisso, a única resposta honesta que podemos dar à pergunta “como será o mundo daqui a cinco anos?” é: Não fazemos ideia. As coisas estão mudando em um ritmo cada vez mais rápido, de modo que é muito difícil — se não impossível — prever como será o nosso mundo daqui a cinco ou dez anos.</p>
								<p>Ora, uma criança que ingressa hoje no sistema educacional estará formada, se tudo correr bem, por volta do ano 2035. 2035! E nós a educamos com base em uma grade curricular inadequada e antiquada, que visa à mera transmissão do conhecimento.</p>
								<p>Então, a pergunta a ser feita — e respondida — é: Para que mundo estamos preparando as nossas crianças? E a única resposta honesta, por mais assustadora que seja, é que não fazemos ideia.</p>
								<p>Neste contexto, podemos concluir que é muito mais importante ensinar as crianças a aprender, a pensar e a resolver problemas, do que obrigá-las a acumular um conhecimento que será, em grande parte, obsoleto quando elas estiverem finalizando sua formação acadêmica.
								A mente humana não foi feita para acumular conhecimento, mas para processar informações e gerar ideias. E o nosso sistema educacional precisa refletir isso.</p>
							</div>
