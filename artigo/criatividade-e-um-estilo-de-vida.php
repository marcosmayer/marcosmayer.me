							<div class="blog-post-titulo-read"><h1>Criatividade é um estilo de vida</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">14 de agosto de 2017</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-criatividade-e-um-estilo-de-vida.jpg"></div>
							<div class="blog-post-texto">
								<p>A criatividade é uma daquelas coisas que a maioria de nós considera algo bacana, mas não essencial. É como se fosse legal ser criativo, mas sem a expectativa de que isso vá fazer uma grande diferença nas nossas vidas.</p>
								<p>A razão para esta visão a respeito da criatividade é, principalmente, a confusão que se faz entre a criatividade, a imaginação e a inovação.</p>
								<p>Quem confunde criatividade com imaginação, provavelmente não dá muito valor a ela, porque a enxerga como algo separado do mundo prático, da dimensão da vida em que as coisas acontecem.</p>
								<p>Quem considera a criatividade muito importante, apenas no seu aspecto mais prático, possivelmente esteja confundindo criatividade com inovação, algo que acontece naturalmente, junto com a evolução da tecnologia.</p>
								<p>Mas a criatividade é outra coisa, muito mais importante e que faz uma diferença muito maior.</p>
								<p>Criatividade é um estilo de vida.</p>
								<p>É claro que, quando a criatividade aparece, com toda sua força e brilho, ela confunde muito a cabeça dos mais desavisados, que acreditam que o que estão vendo à sua frente é um momento, um repente, fruto da inspiração divina.</p>
								<p>Mas a criatividade, quando semeada e cultivada, quando vivida ao longo do tempo, é capaz de nos ajudar a resolver qualquer problema e a viver melhor todas as dimensões da nossa vida.</p>
								<p>Importante observar que não existe criatividade que não seja companheira da curiosidade. Mas isso é assunto para outro texto.</p>
