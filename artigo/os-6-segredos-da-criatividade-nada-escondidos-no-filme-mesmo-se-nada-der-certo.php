							<div class="blog-post-titulo-read"><h1>Os 6 segredos da criatividade nada escondidos no filme Mesmo Se Nada Der Certo</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">11 de abril de 2018</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-os-6-segredos-da-criatividade-nada-escondidos-no-filme-mesmo-se-nada-der-certo.jpg"></div>
							<div class="blog-post-texto">
								<p>O filme “Begin Again” (em português: “Mesmo Se Nada Der Certo”, 2014), com Keira Knightley e Mark Ruffalo, é um filme incrível, que ilustra o pensamento e o processo criativo com precisão. E caos.</p>
								<p>No filme, Gretta James, a personagem de Keira Knightley, é uma ótima compositora, que nunca almejou ser uma estrela, não por falta de talento, apenas porque não. Por acaso, ela encontra Dan Mulligan, a personagem de Mark Ruffalo, um produtor musical que já teve grande sucesso, mas que busca talento autêntico, não algo que possa ser “transformado” em sucesso.</p>
								<p>Não vou contar todo o filme, mas eu sugiro que você leia este artigo é depois assista ao filme. Valerá a pena.</p>
								<p>1. A gente nunca sabe quando vai se deparar com algo incrível: Nem Gretta, nem Dan, planejaram se encontrar e começar algo incrível. Eles estavam apenas vivendo suas vidas quando eles reconheceram a possibilidade de algo acontecer. Reconheceram o diferente. E, para que isso pudesse acontecer, estavam correndo riscos.</p>
								<p>Ninguém pode prever quando algo incrível pode acontecer. Pode ser a qualquer momento. Mas é possível impedir que coisas incríveis aconteçam. É só não fazer nada, não se expor e não correr riscos.</p>
								<p>2. A falha pode ser uma ótima oportunidade para recomeçar, desde que você não desista: Gretta tinha acabado de romper um relacionamento e, com ele, a oportunidade de ser feliz em Nova Iorque. Dan havia sido demitido da gravadora que ele mesmo havia fundado, anos antes. Ela, queria ir embora, de volta para seu país. Ele, foi beber em um bar, para esquecer seus problemas. Seu encontro acabou com suas chances de curtir uma fossa.</p>
								<p>Quando a gente falha, uma possibilidade é desistir. Aliás, se você nunca teve vontade de desistir é porque nunca falhou de verdade, com gosto mesmo. A outra possibilidade é ficar atento para a próxima coisa que vai chamar sua atenção e fazer você sair de seu momento autopiedoso. Aprendeu com a falha? Faça de novo, só que diferente.</p>
								<p>3. Não espere a perfeição para expressar e repartir sua criatividade com o mundo: Se Gretta e Dan fossem esperar as condições ideais para gravar um CD, o filme seria mais longo do que “E O Vento Levou”, de 4 horas de duração. E talvez não terminasse da mesma forma. Em vez de esperar pelo estúdio perfeito, para gravar a demo perfeita, para mostra à gravadora perfeita, eles usaram o que tinham – junto com muita criatividade – e gravaram suas músicas, do jeito que eles queriam que elas fossem, em diversos locais espalhados pela cidade. E fizeram algo fantástico.</p>
								<p>Tem uma ideia? Dê corpo a ela. Mostre a sua ideia. Reparta-a com o mundo. Com as ferramentas que temos disponíveis hoje e com a Internet como meio de distribuição, cada um de nós tem a capacidade de produzir. Tire a sua ideia de dentro da sua cabeça. Quero conhecê-la!</p>
								<p>4. Autenticidade é a maior qualidade de qualquer pessoa criativa: Gretta teve algumas oportunidades de se tornar uma grande estrela, desde que ela se adaptasse e fizesse algumas mudanças: nela e em suas músicas, para tornar o “produto” mais vendável. Mas ela decidiu não se vender, porque suas músicas eram uma expressão de quem ela era. Ela não compunha para alguém. Ela apenas escrevia o que sentia.</p>
								<p>O mundo não precisa de cópias melhoradas ou adaptadas de pessoas ou de ideias que já fizeram muito sucesso. O mundo precisa de autenticidade: ideias originais, expressões de pensamentos novos, frescos, recém saídos de mentes brilhantes, como a sua.</p>
								<p>5. Um pouco de caos e desordem são elementos essenciais no processo criativo: quando Dan reconheceu o talento de Gretta, em um bar onde ela cantou uma de suas músicas, ele estava levemente embriagado. Isso mesmo: bêbado! Mas não a ponto de não perceber a genialidade do que estava ouvindo. Com o pensamento mais… digamos, solto, ele conseguiu deixar sua imaginação voar, ouvindo, em sua cabeça, todos os instrumentos que comporiam a melodia que tornaria aquela canção espetacular.</p>
								<p>No caso de muitos artistas é assim mesmo. Um pouco de embriaguez ajuda no processo. Antes de continuar, quero deixar claro: o meu ponto aqui não é a embriaguez. Não acho que isso funcionaria para todos, nem que todos devam tentar desta forma. Ok? Posso continuar?</p>
								<p>Cada um de nós precisa descobrir como fazer para bagunçar um pouco a nossa mente, saindo de seu estado “certinho”, racional, e entregando-se à divagação, vez ou outra. Um ambiente caótico pode ajudar. Saia de sua rotina, faça as coisas de outro jeito, bagunce a sua mesa de trabalho, descumpra as “regras”, solte sua imaginação. Quem sabe o que pode acontecer? Ninguém! E esta é a beleza do processo criativo.</p>
								<p>6. Não existe essa história de “Não sou criativo”: No filme, a filha de Dan, Violet, interpretada pela atriz Hailee Steinfeld, não vive com seu pai, mas com sua mãe, já que eles são divorciados. Mas a oportunidade surge de Violet tocar um pouco de guitarra em uma das gravações. Claro que ela não acredita que é capaz, mas acaba descobrindo exatamente o contrário. O que acaba sendo, também, uma ótima oportunidade de reaproximar pai e filha.</p>
								<p>Não existe gente não criativa. A criatividade vem de fábrica. Está, ainda que escondida, dentro de cada um de nós. Agora, se você quiser descobrir a sua criatividade, aquilo no que você é capaz de gerar grandes ideias, só tem um jeito: faça coisas, as mais diferentes, o que der na sua cabeça, viva experiências, arrisque-se. Só não vale não fazer nada e dizer que não é capaz.</p>
