							<div class="blog-post-titulo-read"><h1>A criatividade na sala de aula</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">16 de julho de 2016</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-a-criatividade-na-sala-de-aula.jpg"></div>
							<div class="blog-post-texto">
								<p>Já foi o tempo em que se acreditava que ensinar era sinônimo de transmitir conhecimento, apenas. Com base nesta crença, se o aprendizado não acontece, a culpa é do aluno, não do processo pedagógico ou de uma falha na comunicação entre quem ensina e quem aprende.</p>
								<p>Em seu livro, Turning Learning Right Side Up (numa tradução livre, Virando o Lado Certo do Aprendizado para Cima), os autores Russell L. Ackoff e Daniel Greenberg afirmam que “o sistema de educação atual tem uma falha séria — ele é focado no ensino, em vez de no aprendizado”.</p>
								<p>Oscar Wilde afirmou que a “educação é algo admirável, mas é bom lembrar, de tempos em tempos, que nada que vale a pena ser aprendido pode ser ensinado”.</p>
								<p>Neste cenário, a criatividade deveria ter um papel de destaque nas escolas, até porque dizem que ela não pode ser ensinada*. Então deve valer a pena aprendê-la. A criatividade, que nos capacita a resolver problemas, que nos torna protagonistas da nossa história, que nos faz acreditar em nós mesmos e que melhora nossa saúde, mental e física, é essencial para o progresso de uma sociedade.</p>
								<p>Cenin Soling escreveu, em seu brilhante artigo Alguma Escola Consegue Fomentar a Criatividade Pura?, que “a ideia de ensinar criatividade em um ambiente que exige análise, avaliação e pontuação parece improvável, se não impossível”.</p>
								<p>Improvável, sim. Impossível, não.</p>
								<p>O uso de ferramentas como a educomunicação — a educação com o uso da mídia — é capaz de virar o jogo. A grande questão é que, para ser eficaz, ela precisa de um ambiente em que questionar é, além de permitido, incentivado, e onde o espaço para a expressão existe.</p>
								<p>Logo, faz-se necessário que o professor, tão acostumado a “ensinar”, esteja disposto a aprender, abrindo espaço para uma nova relação entre eles e seus alunos, sem hierarquia rígida e aberta ao diálogo, na qual todos aprendem e saem ganhando.</p>
								<p>*Tratamos desse tema em outro artigo, intitulado <a href="https://www.marcosmayer.com.br/artigo/criatividade-pode-ser-ensinada">Criatividade Pode Ser Ensinada, Sim, Senhor!</a></p>
