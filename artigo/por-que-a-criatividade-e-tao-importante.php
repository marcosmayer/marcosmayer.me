							<div class="blog-post-titulo-read"><h1>Por que a criatividade é tão importante?</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">14 de abril de 2018</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-por-que-a-criatividade-e-tao-importante.jpg"></div>
							<div class="blog-post-texto">
								<p>Os resultados alcançados pelas pessoas que decidem desenvolver sua criatividade são impressionantes, e servem como evidência indiscutível da importância de levar a criatividade para a sala de aula, nas escolas e na vida.</p>
								<p>Podemos destacar três mudanças importantes que acontecem na mente dessas pessoas, como parte de sua metanoia, de sua nova forma de enxergar o mundo, de pensar, que leva a uma nova maneira de se relacionar com o mundo, de interagir.</p>
								<p>A primeira mudança é que nós passamos a ter a certeza de que “a vida devia ser bem melhor, e será”, como na música do Gonzaguinha, “O que é, o que é?”.</p>
								<p>É a crença de que a vida não pode ser apenas isso, como escreveu o Rei Salomão: “a vida é uma ilusão, é correr atrás do vento”. De que existe algo de absolutamente incrível, de espantoso, de fabuloso em se viver.</p>
								<p>É viver cada dia esperando que algo incrível aconteça, não esperando no sentido de aguardar, mas no sentido de ter a expectativa de que pode acontecer, o que leva à ação de fazer este “algo” acontecer de fato.</p>
								<p>É como o meu filho, de cinco anos de idade, que, em grande parte dos dias, acorda e diz: “Hoje o dia vai ser muito legal. Acho que vai ser o melhor dia da minha vida”.</p>
								<p>A segunda mudança é a ideia de que você não está neste mundo apenas para passar por ele, sem deixar nenhum tipo de marca—a sua marca.</p>
								<p>É como se nós caminhássemos pela estrada da vida, e esta estrada fosse na areia da praia, em que você pisa, deixa uma marca, mas logo vem a onda e apaga seus passos.</p>
								<p>É passar a acreditar—e a agir de acordo com esta crença—que você nasceu para ser protagonista na história, e não apenas um coadjuvante, um extra, que faz uma pontinha em um filme, que ninguém nota e que não faz nenhuma diferença importante.</p>
								<p>A terceira mudança é conquista da autoconfiança necessária para que você seja o piloto da sua própria vida, e não o copiloto. Que você esteja dirigindo e carro que representa a sua vida, e não esteja apenas sentado no banco do passageiro.</p>
								<p>É a noção de que você mesmo define a sua sorte e o seu destino. Que você faz acontecer a sua história. Que você controla a sua própria vida, que você é o autor da sua própria história.</p>
								<p>Como disse Brené Brown: “Quando você controla a sua história, você pode escrever o final dela”.</p>
								<p>Por isso é que eu dou tanta importância à criatividade, como uma das capacidades mais importantes que devemos desenvolver na vida.</p>
