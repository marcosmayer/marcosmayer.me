							<div class="blog-post-titulo-read"><h1>Meu Pai — Fonte de inspiração</h1></div>
							<div class="blog-info">
								<div class="blog-post-data">8 de janeiro de 2015</div>
								<div class="blog-post-autor">Marcos Mayer</div>
							</div>
							<div class="blog-post-imagem"><img src="/images/blog/sm-meu-pai-fonte-de-inspiracao.jpg"></div>
							<div class="blog-post-texto">
								<p>Eu me considero uma pessoa bastante criativa. E digo isso tranquilo, sem correr o risco de estar sendo arrogante, porque reconheço que quando alguém é criativo, apenas uma parte do mérito é sua. Dificilmente alguém é criativo sem que tenha sido criado em um ambiente adequado para o desenvolvimento da criatividade. E eu fui criado em um ambiente assim.</p>
								<p>Hoje faz um ano que meu Pai nos deixou, aos 78 anos. Ele viveu uma vida interessante, pra dizer o mínimo. Então eu passei o dia pensando muito nele, e achei que devia dar o devido crédito a alguém que foi fonte de tanta inspiração para mim.</p>
								<p>Peter Mayer. Esse era seu nome. Pra começar, ele nasceu na Romênia, na região da Transilvânia. Já começou bem. He He Lá pelos 7 anos, fugiu, com sua família, da guerra, a 2ª mundial. Largaram tudo que tinham e foram para a Áustria. Lá, com uma situação financeira pra lá de ruim, meu Pai comeu comida do lixo e comida "roubada" dos porcos da fazenda onde sua mãe trabalhava. Sim, porque era ela que trabalhava na fazenda, então ela era alimentada. Seus três filhos não. Onde morava, escutava os mísseis das forças aliadas voando por cima da sua cabeça e explodindo em algum lugar não distante o suficiente. Até que o exército americano apareceu em sua cidade, confirmando o fim da guerra, por meio de um soldado que tirou um chocolate de sua farda para presentear um garoto esfomeado, que foi, assim, eternamente conquistado.</p>
								<p>Lá pelos 15 anos meu avô decidiu trazer a família para o Brasil, ao estado do Paraná, para colonizar as terras que estavam destinadas a esse fim. Vieram de navio, passando semanas no mar, para chegar à terra na qual meu Pai estabeleceria sua vida. Depois de mais alguns anos, vieram para São Paulo.</p>
								<p>Eu tentei desenhar um pouco o cenário da criação do meu Pai para que você possa tentar compreender um pouco melhor a pessoa que eu conheci como meu Pai. Afinal, eu não conheci o menino faminto e assustado do interior da Áustria. Eu conheci um Pai cheio de manias, com algumas características bem peculiares, e conhecer seu passado me ajudou a compreender melhor o seu jeito.</p>
								<p>Acontece que, por alguma razão, meu Pai acabou se tornando um cara extremamente criativo. Ele contrariou algumas máximas bem importantes. Por exemplo: a de que você só será muito bom em algo se o fizer com paixão. Veja, meu Pai odiava sua profissão. Não era ele que a havia escolhido, mas o meu avô. Ele era técnico têxtil. Mas mesmo odiando o que fazia, meu Pai faltou 0 (zero) dias no trabalho. Nunca uma doença o afastou, por um dia sequer, do seu local de trabalho. Pontualmente, estava lá. E o cara era bom no que fazia. Foi considerado por muitos o melhor técnico têxtil do Brasil. Ainda hoje, sua fama nas empresas têxteis do Brás é quase mítica e seu nome, muito respeitado.</p>
								<p>Mas sua criatividade brilhava mesmo quando ele fazia coisas manuais, nas quais era genial, fora do trabalho. Meu Pai construiu algumas das casas onde moramos, sozinho. E não, ele não era - nem sonhava ser - pedreiro. Ele apenas decidiu que o faria, e melhor do que qualquer pedreiro profissional. E fez mesmo. Demorava muito para fazê-lo, evidentemente, o que nunca agradou muito a minha mãe, mas o fazia como poucos. E toda vez que surgia algum novo desafio, um problema a ser resolvido no processo de construção, que ele não dominava, ele ficava matutando, estudando e falando - falando muito! - sobre o assunto, até que a solução surgia, e ele voltava a falar - e a falar muito! - de como resolveria o problema.</p>
								<p>Ele também cuidava da mecânica de seus próprios carros. Imagine desmontar um carburador, com centenas de peças (pareciam centenas para mim), que ele dedicadamente desmontava, tratava e fazia funcionar melhor. Ele também achava que faria melhor do que a maioria dos mecânicos. E fazia mesmo.</p>
								<p>Depois que se aposentou, meu Pai continuou trabalhando para a indústria têxtil, mas como um fabricante de peças para máquinas. Sua oficina era bem simples, com algumas máquinas usadas - nem todas as que seriam necessárias - que ele havia adquirido. Mas ele "tirava água de pedra" com aquela oficina. Chegava uma nova encomenda, de uma peça que precisava ser fabricada, 100 ou 200 unidades. Peças complexas, com detalhes difíceis de serem fabricados. E meu Pai passava alguns dias sem saber exatamente o que fazer. Levava a peça modelo nas reuniões de família e conversava com todos e com qualquer um a respeito das dificuldades de se fabricar aquela peça. Depois de alguns dias, a solução era encontrada e meu Pai começava a mágica.</p>
								<p>Ainda nessa época, já aposentado, se alguma fábrica de tecido tivesse problemas com alguma máquina de fabricação mais antiga, daquelas que fabricam quilômetros de tecidos diversos, e, depois de muitos tentarem, ninguém conseguisse resolver o problema daquela máquina, meu Pai era chamado, mesmo que fosse em outro estado. Acho que ele passava um tempo conversando com a máquina, para entendê-la. Não sei. Mas o fato é que ele resolvia o problema e a máquina voltava a funcionar.</p>
								<p>Assim era o meu Pai. Se alguém perguntasse a ele se ele era criativo, não sei o que ele responderia. Talvez dissesse que não era. Mas ele era, e muito. Eu poderia passar horas contando história sobre o meu Pai.</p>
								<p>Mas o que eu sei mesmo é que ele foi muito importante na minha formação como pessoa e como profissional. E foi um grande responsável pela minha criatividade.</p>
								<p>Então é isso. Fica aqui registrada a minha homenagem ao meu Pai. Um grande cara, que teve grande influência sobre quem eu me tornei. Valeu, Pai!</p>
								<p>Acho que ele só não teve tanta influência na minha criatividade quanto a minha mãe. Mas essa história fica pra outro dia.</p>
