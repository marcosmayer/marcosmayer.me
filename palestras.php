				<p>Todo mundo já ouviu ou até participou de alguma conversa sobre o futuro, sobre como ele está chegando para aterrorizar nossas vidas e tomar nossos empregos. Filmes como "O Exterminador de Futuro" 1, 2, 3, 4, 5 e 6 (que ainda está no futuro) ajudaram a formar essa visão assustadora sobre o que virá pela frente.</p>
				<p>A verdade é que ninguém sabe, exatamente, como será o futuro. Muitas previsões são feitas, algumas até com certo grau de acuracidade, mas o futuro sempre nos surpreende. Então como que é que a gente pode se preparar para esse futuro, se não sabemos como ele será? Só existe um caminho para isso.</p>
				<p>Para estarem preparados para o futuro, profissionais e empresas devem desenvolver a única habilidade que não leva em consideração nenhuma previsão, mas que nos deixa prontos para enfrentar o que quer que seja jogado na nossa direção: a criatividade. A criatividade é uma habilidade que nos leva a acreditar que somos capazes de descobrir e definir um problema, qualquer que seja ele, e a encontrar uma solução para ele, com ideias novas e originais.</p>
				<p>Essa apresentação trata da relação entre a criatividade, o futuro e o trabalho, e sobre como podemos nos preparar hoje para não ser pegos de surpresa por nada do que possa vir. Como escreveu Dennis Gabor, em seu livro “Inventando o Futuro”, de 1963, <span class="destaque">“o futuro não pode ser previsto, mas futuros podem ser inventados”</span>. O meio de inventar o futuro é a inovação.</p>
