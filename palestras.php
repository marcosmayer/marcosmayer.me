			<div class="produto-texto-area">
				<p>Todos temos alguém que admiramos, alguém em quem nos espelhamos e alguém que nos inspira. No ambiente de trabalho, chamamos esse alguém de case. Todo profissional ou empresa que se torna um case, passa a ser uma referência, citada por muitos como tendo alcançado um resultado excepcional, fora da curva, e passa a ser estudado, com todo mundo tentando entender o que é que deu tão certo, para que possa ser repetido.</p>
				<p>O problema é que, na maior parte dos casos, nós olhamos para essa pessoa ou empresa como sendo muito superior, totalmente fora do nosso alcance, o que é uma grande mentira. Em vez disso, deveríamos trabalhar para nos tornarmos, nós mesmos, a pessoa ou a empresa que mais admiramos. Por que não seria assim?</p>
				<p>Ser o seu próprio case é decidir que você quer tornar-se a pessoa que você mais admira e, a partir dessa decisão, trabalhar para tornar isso realidade. Ser o seu próprio case não é “se achar”, porque quem se acha, não é, e quem é, não precisa se achar. Ser o seu próprio case é confiar em você mesmo, é adaptar-se rapidamente a mudanças, é solucionar os problemas que aparecem e é expressar plenamente aquilo que você é. Essas são as capacidades mais importantes para profissionais e empresas que querem ser competitivos e relevantes, hoje e no futuro.</p>
				<p>O objetivo dessa palestra é mostrar às pessoas que elas podem tornar-se a pessoa que mais admiram. E isso muda completamente o jogo. Uma pessoa que é capaz de confiar em si mesmo, de adaptar-se a mudanças, de solucionar problemas e de expressar-se plenamente, tem uma qualidade de vida muito melhor, é muito mais feliz e é capaz de realizar qualquer coisa. É uma pessoa que não vai apenas sobreviver ou apenas passar pela vida, mas vai viver e experimentar todo seu potencial, que é virtualmente infinito.</p>
