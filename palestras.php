			</div>
			<div class="produto-chamada-area">
				<h3 class="produto-chamada">Como gerar um processo contínuo de geração e implementação de ideias originais</h3>
			</div>
			<div class="produto-texto-area">
				<p>A inovação é a criatividade aplicada, é o resultado visível de um processo criativo, que muda para sempre o jeito como fazemos as coisas e deixa a nossa vida muito melhor. São os profissionais e as empresas inovadoras que escrevem a história e definem como será o futuro. Sempre foi assim e continua sendo, cada vez mais.</p>
				<p>A inovação pode acontecer na forma de um novo produto. Esse é o tipo de inovação mais fácil de identificar. Mas ela também pode surgir na forma de um novo serviço, uma nova tecnologia, um novo processo ou um novo jeito de se relacionar com clientes. De qualquer forma, ela sempre é fruto da combinação de uma ideia original com uma atitude de líder. Empresas inovadoras distanciam-se da competição, em vez de tentar disputar cada palmo de espaço.</p>
				<p>Como uma empresa ou um profissional podem ser inovadores? E mais: como elas podem continuar sendo inovadores? Como a inovação pode tornar-se parte da cultura de uma empresa, o que é uma característica das empresas líderes, para as quais sempre olhamos esperando a próxima coisa?</p>
				<p>Essa palestra apresenta todo o cenário da inovação, desde como ela funciona, até como ela vira parte do DNA de um profissional ou empresa. Ela trata de como a inovação pode extrapolar, em muito, a intenção de gerar lucro, para gerar mudança, gerar progresso, e ter um significado na vida das pessoas.</p>
