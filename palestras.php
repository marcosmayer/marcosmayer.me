				<p>Vamos ser honestos: a criatividade ainda não é levada a sério no Brasil, nem por profissionais, nem por empresas. Essa afirmação tem dois limites, certo? O de tempo, que diz que ela <span class="destaque">ainda</span> não é levada a sério, e o de espaço, que diz que ela não é levada a sério <span class="destaque">no Brasil</span>. Acontece que este é exatamente o caso. Estudos realizados em diversas partes do mundo revelam que a criatividade é considerada o fator mais importante para o sucesso no futuro, tanto para profissionais, quanto para empresas.</p>
				<p><span class="destaque">Ed McCabe</span>, o profissional mais jovem, na história, a ser eleito para o <span class="destaque">Hall da Fama One Club</span>, que reconhece e premia a excelência na publicidade, afirmou que “a criatividade é um dos últimos meios legais de se ganhar uma vantagem desleal sobre a competição”. A verdade é que profissionais e empresas que tiram proveito de todos os benefícios da criatividade estarão sempre em vantagem.</p>
				<p>O objetivo dessa palestra é mostrar a importância da criatividade, apresentando-a da forma como ela deve ser entendida, e ensinar alguns meios de como desenvolver e promover a cratividade no trabalho. Afinal, por que a criatividade deve ser levada a sério e receber muito mais espaço na agenda de profissionais e empresas? E como eles podem acessar todo o potencial criativo e transformá-lo em potência?</p>
