				<h2><span>Nossa Proposta</span></h2>
				<p>Acreditamos que todas as pessoas têm um grande potencial para viver de forma completa e satisfatória, e que a escola tem um papel fundamental a desempenhar na trajetória de cada indivíduo.</p>
				<p>Queremos ser parceiros das escolas e de outras organizações ligadas à educação, na busca por soluções criativas para tornar a experiência de cada aluno mais relevante e de maior impacto positivo em sua vida, como indivíduo, como parte de uma comunidade, e na sociedade como um todo.</p>
				<p>Queremos nos unir a professores e gestores escolares na batalha diário que é melhorar o sistema de ensino no Brasil para quem mais nos interessa, que são os nosso alunos, crianças e jovens que têm o potencial de transformar o mundo em que vivemos. Propomos fazer isso atuando, principalmente, em duas frentes:</p>
				<p><span class="destaque">Uma educação mais criativa</span></p>
				<p>Aqui estamos falando de usar mais criatividade para transmitir o conhecimento aos nossos alunos, despertando seu interesse na busca pelo aprendizado. Descobrir e aprender são experiências incríveis, e nós temos que ajudá-los a perceber isso.</p>
				<p><span class="destaque">Mais criatividade na educação</span></p>
				<p>Aqui estamos falando de inserir o desenvolvimento da criatividade como um aprendizado de extrema importância para quem se prepara não apenas para o mercado de trabalho, mas para viver a vida da melhor maneira possível, explorando todo seu potencial.</p>
