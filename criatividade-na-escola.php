				<h2><span>Sobre a Educação</span></h2>
				<p>O sistema de ensino atual foi pensado em meados do século XIX, para atender à demanda gerada pela <span class="bold">Revolução Industrial</span>, que aconteceu entre 1760 e 1840, aproximadamente. É um sistema que visa à <span class="bold">padronização</span> dos estudantes e que busca, sem sucesso, prepará-los para algumas poucas profissões.</p>
				<p>Além disso, uma criança que entra hoje no sistema de ensino deve formar-se daqui a 18 ou 20 anos, e ninguém faz ideia de como será o mundo daqui a 20 anos. É bem possível que a maior parte das profissões que existirão nem foram criadas ainda. Como, então, podemos preparar uma criança para uma realidade que não sabemos como será?</p>
				<p>Precisamos de um sistema de ensino repensado para a nossa realidade do século XXI, centrado nas crianças, que prepare os estudantes para serem o melhor que podem ser, independentemente do que escolherem ser. Propomos tornar a educação mais criativa e, principalmente, inserir a criatividade na educação.</p>
				<p>A ideia de transformar o nosso sistema de ensino interessa a todas as pessoas envolvidas, de alguma forma, com a educação de crianças e jovens. Estamos falando de professores, gestores escolares e dirigentes educacionais de todas as instâncias do governo. Mas também estamos falando de pais e todos que, de alguma forma, são responsáveis pela criação de uma ou mais crianças.</p>
				<p>O que pode ser feito?</p>
				<p>Quem está envolvido com a educação, de alguma forma, tem três opções. É possível fazer mudanças dentro do sistema, pressionar por mudanças no sistema em si ou tomar iniciativas fora do sistema. Nós precisamos das três coisas. Um mudança no sistema passa, necessariamente, por mudanças nas políticas públicas de educação. Acontece que, se observarmos através da história, as mudanças mais importantes nunca acontecem por iniciativa dos governos. Os governos apenas reagem a movimentos legítimos que expressem o desejo de mudança da população.</p>
