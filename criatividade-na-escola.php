				<h2><span>O CASE pela criatividade</span></h2>
				<p>A criatividade é uma das capacidades mais importantes que uma criança pode desenvolver. Uma capacidade que pode mudar completamente o rumo de sua vida e ser decisiva para o seu futuro. A razão é simples. É que a criatividade prepara uma criança para qualquer futuro que ela vá encontrar. O desenvolvimento da criatividade prepara a criança para escrever a própria história e para ser dona de seu destino.</p>
				<p>O programa <a class="a-over-dark" href="seja-o-seu-case/" title="Seja o Seu CASE">Seja o Seu CASE</a> trata exatamente do desenvolvimento da criatividade e dos principais benefícios trazidos por um estilo de vida criativo. Uma pessoa criativa confia em si mesma, consegue adaptar-se às mudanças, é capaz de solucionar problemas e sabe expressar-se de forma original e autêntica.</p>
