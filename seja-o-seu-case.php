				<h2><span>Como nasceu o C.A.S.E.</span></h2>
				<p>Conforme Marcos estudava o tema da criatividade, ficava cada vez mais convencido de sua importância. Começou a escrever e a falar sobre o tema, nas redes sociais e em palestras que dava pelo Brasil. Nessa caminhada, percebeu que a urgência na ampla divulgação da importância da criatividade não era compartilhada pela maior parte das pessoas. Aliás, pouquíssimas pessoas tratavam o tema com sua devida importância.</p>
				<p>Foi então que Marcos tentou descobrir o motivo de as pessoas não levarem a criatividade a sério. Além de uma série de mitos que cercam o tema e que atrapalham muito na sua compreensão, ele descobriu que falta uma abordagem mais prática e clara de quais são os principais benefícios de um estilo de vida criativo. Faltava as pessoas saberem o que fazer com ela. E isso direcionou sua pesquisa, para tentar responder a esta questão: quais são os principais benefícios da criatividade?</p>
				<p>A resposta que Marcos encontrou foi o C.A.S.E., um acrônimo que representa as quatro principais capacidades que são desenvolvidas quando passamos a levar um estilo de vida criativo. São elas: confiar (em si mesmo), adaptar (adaptar-se às mudanças), solucionar (problemas) e expressar (sua verdadeira identidade). Essas quatro capacidades permitem a um ser humano viver plenamente, sendo capaz de construir uma vida com sentido e senso de realização. Em última instância, permitem que sejam felizes.</p>
