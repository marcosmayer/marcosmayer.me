				<h2><span>Imaginação, Curiosidade e Criatividade</span></h2>
				<p>Há alguns anos, fui convidado a falar sobre criatividade para os alunos de uma escola. Fiquei muito satisfeito com a oportunidade. Falar sobre criatividade no ambiente da Educação é algo que faço com muito prazer e alegria. Só havia um problema: o meu público seria formado por crianças que iam do Infantil ao Fundamental 2, que tinham idades entre 3 e 15 anos.</p>
				<p>Dividimos os estudantes em três turmas, naturalmente: Infantil, Fundamental 1 e Fundamental 2. Isso já ajudou bastante, mas também significava que eu teria um enorme desafio pela frente: teria que dar uma palestra sobre criatividade para crianças com 3, 4 e 5 anos de idade. Para os mais velhos, do Fundamental 2, era tranquilo. Criatividade na veia! Mas se eu fosse falar sobre criatividade para as crianças mais novas, explicando o que é ser criativo e por que a criatividade é tão importante, as crianças me olhariam com cara de “ué?” e me perguntariam algo que seria muito difícil responder. Elas diriam: “E existe outro jeito?”. E eu teria, então, que explicar a elas que, quando elas crescerem um pouco mais, elas serão ensinadas a deixar a imaginação e a curiosidade para trás, o que vai afetar diretamente a sua capacidade de viver de forma criativa. Daí elas me perguntariam por que é que alguém faria isso, e eu não teria nenhuma boa resposta para elas.</p>
				<p>Lembrei de alguns desenhos que assistia com meus filhos, em especial de “O Show da Luna” e “Meu Amigãozão”. O primeiro, uma produção brasileira, já exportada para dezenas de países, trata da curiosidade das crianças, enquanto o segundo, produzido por um estúdio brasileiro, em parceria com outro canadense, usa e abusa da imaginação. Aí não tive dúvidas. Falei sobre imaginação para as crianças do Infantil, sobre curiosidade para as do Fundamental 1 e sobre criatividade para as do Fundamental 2.</p>
				<p>Além de ter sido muito natural falar sobre esses temas para as diversas faixas etárias, ter estudado o assunto e pensado sobre ele dessa forma, como uma sequência entre a imaginação, a curiosidade e a criatividade, fez muito sentido. Ficou claro por que as crianças são tão criativas e ficou claro como os adultos perdem sua capacidade criativa: nós começamos abandonando a imaginação, que é “coisa de criança”, depois deixamos de lado a curiosidade e, por fim, como consequência, deixamos de viver de forma criativa.</p>
